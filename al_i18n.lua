function I18N.t(str, ...)
    local l = I18N.getLang()
    if not strings[l] then l = "en" end
    
    local translated = str
    if strings[l] and strings[l][str] then
        translated = strings[l][str]
    elseif strings["en"][str] then
        -- fallback to English if not found in current lang
        translated = strings["en"][str]
    end
    
    local args = {...}
    if #args > 0 then
        for i, v in ipairs(args) do
            translated = translated:gsub("%%" .. i, tostring(v))
        end
    end
    
    return translated
end

return I18N
