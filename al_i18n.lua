end

function I18N.getLang()
    if I18N.current_lang and I18N.current_lang ~= "Auto" then
        return I18N.current_lang
    end
    local lang = "en"
    if G_reader_settings then
        lang = G_reader_settings:readSetting("language") or "en"
    end
    return lang:sub(1, 2)
end

